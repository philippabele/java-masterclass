package Section03FirstSteps;

public class RecapPrimitivesAndString {
    public static void main(String[] args) {
        // byte
        // short
        // int
        // long
        // float
        // double
        // char
        // boolean

        String myString = "This is a string.";
        myString = myString + " With a second sentence.";
        System.out.println(myString);

        int myInt = 10;
        myString = myString + "May we add a int to this string? " + myInt;
        System.out.println(myString);
    }
}
